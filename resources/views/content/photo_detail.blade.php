                <p>Sebutan “Badui” merupakan sebutan yang diberikan oleh penduduk luar kepada kelompok masyarakat yang bermukim di pedalaman Provinsi Banten. Para peneliti Belanda kemungkinan menyamakan mereka dengan suku Arab Badawi, atau yang lebih dikenal dengan nama “Bedouin.” Kata “Baduy” kemungkinan berasal dari kata “Bedouin,” tapi sumber lain mengatakan kalau nama mereka berasal dari nama sungai setempat.</p>
                <p>Berdasarkan Kamus Besar Bahasa Indonesia, penulisan yang tepat adalah “Badui” dan bukan “Baduy”. Meskipun begitu, mereka sendiri lebih suka menyebut diri sebagai urang Kanekes atau “orang Kanekes” sesuai dengan nama wilayah mereka, atau sebutan yang mengacu kepada nama kampung mereka seperti Urang Cibeo.</p>
                <p>Orang Kanekes memiliki hubungan sejarah dengan orang Sunda. Penampilan fisik dan bahasa mereka mirip dengan orang-orang Sunda pada umumnya. Satu-satunya perbedaan adalah kepercayaan dan cara hidup mereka. Orang Kanekes menutup diri dari pengaruh dunia luar dan secara ketat menjaga cara hidup mereka yang tradisional, sedangkan orang Sunda lebih terbuka kepada pengaruh asing dan mayoritas memeluk Islam.</p>
